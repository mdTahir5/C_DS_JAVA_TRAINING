package Arrays;

public class FriendsNameUsingArray {
    public static void main(String[] args) {
        String[] friendNames = {"Fahad", "Faraz" , "Faisal" , "Zaid" , "Altamash"};
        // for each loop
        for (String Name : friendNames) {
            System.out.println(Name);
        }
    }
}
